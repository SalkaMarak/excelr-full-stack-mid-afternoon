class A 
{
	public static void main(String[] args) 
	{
		int i = true ? 10 : 20; //3 parts
		System.out.println(i);
	}
}

//question mark is wild card character, if true first part will be executed, ie 10. if false control will execute the second part that is 20.

// : is a separator

//ternary means three which include the condition
