class I 
{
	public static void main(String[] args) 
	{
		int i = 10;
		switch(i) 
		{
			case 2:
				System.out.println("from case 2");
				break;
			case 3:
				System.out.println("from case 3");
				break;
			default:
				System.out.println("from default2");
			default:
				System.out.println("from default2");
		}
	}
}

//we cannot duplicate cases with the same name or have two default cases.



