class I 
{
	static byte i;
	static boolean j = true, m = false, k;
	static char ch;
	public static void main(String[] args) 
	{
		System.out.println(I.i);
		System.out.println(I.j);
		System.out.println(I.m);
		System.out.println(I.k);
		System.out.println(I.ch);
	}
}

